<template>
  <div class="container">
    <el-card>
      <div solt="header">
        <mybread>评论管理</mybread>
      </div>
      <el-table :data="comments">
        <el-table-column label="标题" prop="title"></el-table-column>
          <el-table-column label="总评论数" prop="total_comment_count"></el-table-column>
        <el-table-column label="粉丝评论数" prop="fans_comment_count"></el-table-column>
        <el-table-column label="状态">
          <template slot-scope="scope">{{scope.row.comment_status?'正常':'关闭'}}</template>
        </el-table-column>
        <el-table-column label="操作" width="160px">
            <template slot-scope="scope">
             <el-button @click="toggleStatus(scope.row)" v-if="!scope.row.comment_status" type="success" size="small">打开评论</el-button>
             <el-button @click="toggleStatus(scope.row)" v-else type="danger" size="small">关闭评论</el-button>
            </template>
        </el-table-column>
      </el-table>
    <el-pagination
        background
        style="text-align:center;margin-top:30px;"
        layout="prev, pager, next"
        :total="total"
        :page-size="reqParams.per_page"
        v-if="total>reqParams.per_page"
        :current-page="reqParams.page"
         @current-change="changePager"
      ></el-pagination>
    </el-card>
  </div>
</template>

<script>
export default {
  data () {
    return {
      reqParams: {
        response_type: 'comment',
        page: 1,
        per_page: 20
      },
      comments: [],
      total: 0
    }
  },

  created () {
    this.getComment()
  },
  methods: {
    //   获取评论数据
    async getComment () {
      const { data: { data } } = await this.$http.get('articles', { params: this.reqParams })
      this.comments = data.results
      this.total = data.total_count
    },
    //   页码的点击切换事件
    changePager (newpage) {
      this.reqParams.page = newpage
      this.getComment()
    },
    // 打开铁环评论功能
    async toggleStatus (row) {
      const { data: { data } } = await this.$http.put(`comments/status?article_id=${row.id}`, {
        allow_comment: !row.comment_status })
      this.$message.success(data.allow_comment ? '打开评论成功' : '评论关闭成功')
      row.comment_status = data.allow_comment
    }
  }
}
</script>
